# To slice out the first three companies from the it_companies list, you can use list slicing.
it_companies = ['Facebook', 'Google', 'Microsoft', 'Apple', 'IBM', 'Oracle', 'Amazon']

print("Before Slicing: ",it_companies)
# Slice out the first 3 companies
first_three_companies = it_companies[:3]

# Print the result
print("First three companies:", first_three_companies)
