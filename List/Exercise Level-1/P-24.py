# To remove all IT companies from the it_companies list, you can use the clear() method to remove all elements from the list
it_companies = ['Facebook', 'Google', 'Microsoft', 'Apple', 'IBM', 'Oracle', 'Amazon']
print("Before Removing: ",it_companies)

# Remove all IT companies using clear()
it_companies.clear()

# Print the updated list
print("Updated list of IT companies:", it_companies)
