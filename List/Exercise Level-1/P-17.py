# To reverse the it_companies list in descending order, you can first use the sort() method to sort it in ascending order 
# and then apply the reverse() method. 

it_companies = ['Facebook', 'Google', 'Microsoft', 'Apple', 'IBM', 'Oracle', 'Amazon']
print ("Before Reversing : ", it_companies)
# Sort the list in ascending order
it_companies.sort()

# Reverse the list to get descending order
it_companies.reverse()

# Print the reversed list
print("Reversed list in descending order:", it_companies)
