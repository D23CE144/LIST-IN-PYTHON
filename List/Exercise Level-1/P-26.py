# You can join the front_end and back_end lists in Python using the + operator. 
front_end = ['HTML', 'CSS', 'JS', 'React', 'Redux']
back_end = ['Node', 'Express', 'MongoDB']

# Join the lists
full_stack = front_end + back_end

# Print the result
print("Full Stack:", full_stack)
# The + operator concatenates the two lists, creating a new list containing all the elements from both front_end and back_end.