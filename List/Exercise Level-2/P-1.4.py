ages = [19, 22, 19, 24, 20, 25, 26, 24, 25, 24]

# Calculate the average age
average_age = sum(ages) / len(ages)

# Print the result
print("Ages:", ages)
print("Average age:", average_age)
# sum(ages) calculates the sum of all ages, and len(ages) gives the number of ages in the list. 
# The average age is then calculated by dividing the sum by the number of items.