# To find the range of the ages (the difference between the maximum and minimum age), you can use the max() and min() functions.
ages = [19, 22, 19, 24, 20, 25, 26, 24, 25, 24]

# Calculate the range of ages
age_range = max(ages) - min(ages)

# Print the result
print("Ages:", ages)
print("Range of ages:", age_range)
# max(ages) gives the maximum age, min(ages) gives the minimum age, 
# and the difference between them (max(ages) - min(ages)) gives the range of ages.