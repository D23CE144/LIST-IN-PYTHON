# You can sort the ages list using the sort() method and then find the minimum and maximum age using the min() and max() functions.
ages = [19, 22, 19, 24, 20, 25, 26, 24, 25, 24]

# Sort the list
ages.sort()

# Find the min and max age
min_age = min(ages)
max_age = max(ages)

# Print the results
print("Sorted ages:", ages)
print("Min age:", min_age)
print("Max age:", max_age)
